const num = 90;
// constantes são variáveis que não mudam  // pode receber variavel ou função
console.log('Num: ' + num);

// nao se recomenda mais utilizar var, pois no lugar dele temos o let
// escopo do let é do local de onde ele foi declarado
let num2 = 40;
console.log(num2);
num2 = 50;
console.log(num2);
